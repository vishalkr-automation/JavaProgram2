package programs;

// how do you convert a string to Upper case and to Lower case
public class change_Lower_Uppercase_String {

	public static void main(String[] args) {
		String s1="HELOO";
		String s2="world";
		
		//To Lower case
		String lower=s1.toLowerCase();
		System.out.println(lower);
		
		//To uper case
		String upper=s2.toUpperCase();
		System.out.println(upper);
	}

}
